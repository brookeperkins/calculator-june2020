export function add(value1, value2) {
    return Number(value1) + Number(value2);
}

export function subtract(subvalue1, subvalue2) {
    return Number(subvalue1) - Number(subvalue2);
}

export function multiply(multvalue1, multvalue2) {
    return Number(multvalue1) * Number(multvalue2);
}

export function divide(dividevalue1, dividevalue2) {
    return Number(dividevalue1) / Number(dividevalue2);
}