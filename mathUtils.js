export function addition(input1, input2) {
    return input1 + input2; 
}

export function subtraction(input1, input2) {
    return input1 - input2; 
}

export function multiplication(input1, input2) {
    return input1 * input2; 
}

export function division(input1, input2) {
    return input1 / input2; 
}
